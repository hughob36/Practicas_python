'''
Crear un programa que pida un número de mes (ejemplo 4) y escriba el nombre del
mes en letras ("abril"). Verificar que el mes sea válido e informar en caso que no lo sea.

'''
print()

numero = int((input("Ingrese número del mes : ")))

print()

if numero == 1:
    print(f"El mes {numero} es enero.")

elif numero == 2:
     print(f"El mes {2} es febrero.")

elif numero == 3:
     print(f"El mes {3} es marzo.")

elif numero == 4:
     print(f"El mes {4} es abril.")

elif numero == 5:
     print(f"El mes {5} es mayo.")

elif numero == 6:
     print(f"El mes {6} es junio.")

elif numero == 7:
     print(f"El mes {7} es julio.")

elif numero == 8:
     print(f"El mes {8} es agosto.")

elif numero == 9:
     print(f"El mes {9} es septiembre.")

elif numero == 10:
     print(f"El mes {10} es octubre.")

elif numero == 11:
     print(f"El mes {11} es noviembre.")
     
elif numero == 12:
     print(f"El mes {12} es diciembre.")
     
else:
    print("El mes número tipeado no corresponde a ningun mes.")

print() 

